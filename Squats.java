import java.util.*;
class Codechef
{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int t=sc.nextInt();
        while(t>0)
        {
            int x,s;
            x=sc.nextInt();
            s=x*15;
            System.out.println(s);
            t--;
        }
    }
}