import java.util.*;
class Codechef
{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int t=sc.nextInt();
        while(t-- >0)
        {
            int x,y,a;
            x=sc.nextInt();
            y=sc.nextInt();
            a=x-y;
            System.out.println(a);
        }
    }
}