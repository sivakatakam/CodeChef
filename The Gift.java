import java.util.*;
class Codechef
{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int x=sc.nextInt();
        int n=sc.nextInt();
        int m=sc.nextInt();
        if(x+m>=n)
        System.out.println("YES");
        else
        System.out.println("NO");
    }
}